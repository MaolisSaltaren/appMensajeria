
package modelo;


public class CiudadesModel {
    
    public  String ciudad_nombre;
    public  String depa_nombre;

    public String getCiudad_nombre() {
        return ciudad_nombre;
    }

    public void setCiudad_nombre(String ciudad_nombre) {
        this.ciudad_nombre = ciudad_nombre;
    }

    public String getDepa_nombre() {
        return depa_nombre;
    }

    public void setDepa_nombre(String depa_nombre) {
        this.depa_nombre = depa_nombre;
    }
    
            
}
